#include "libft.h"

char	*ft_strdup(const char *s)
{
	char	*d;
	size_t	size;
	size_t	i;

	size = ft_strlen(s);
	d = malloc(size + 1);
	if (d == NULL)
		return (NULL);
	i = 0;
	while (s[i])
	{
		d[i] = s[i];
		i++;
	}
	d[i] = '\0';
	return (d);
}
