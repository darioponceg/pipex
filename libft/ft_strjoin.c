#include "libft.h"

char	*ft_strjoin(char const *s1, char const *s2)
{
	char	*s3;
	size_t	size_s1;
	size_t	size_s2;
	size_t	i;
	size_t	j;

	size_s1 = ft_strlen(s1);
	size_s2 = ft_strlen(s2);
	s3 = malloc(size_s1 + size_s2 +1);
	if (s3 == NULL)
		return (NULL);
	i = 0;
	while (s1[i])
	{
		s3[i] = s1[i];
		i++;
	}
	j = 0;
	while (s2[j])
	{
		s3[i + j] = s2[j];
		j++;
	}
	s3[i + j] = '\0';
	return (s3);
}
