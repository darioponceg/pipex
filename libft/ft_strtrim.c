#include "libft.h"

char	*ft_strtrim(char const *s1, char const *set)
{
	char	*s3;
	size_t	start;
	size_t	end;

	if (s1 == NULL || set == NULL)
		return (NULL);
	start = 0;
	while (s1[start] && ft_strchr(set, s1[start]))
		start++;
	end = ft_strlen(s1);
	while (end > start && ft_strchr(set, s1[end - 1]))
		end--;
	s3 = malloc(end - start + 1);
	if (s3 == NULL)
		return (NULL);
	ft_strlcpy(s3, s1 + start, end - start + 1);
	return (s3);
}
